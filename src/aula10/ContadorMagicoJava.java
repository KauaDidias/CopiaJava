package aula10;

public class ContadorMagicoJava {

		public static void main(String[] args) {
		
			System.out.println("Contagem crescente:");
			for (int n = 1; n <= 10; n++) {
			System.out.println("A contagem está em " + n); }
			
			System.out.println("Contagem decrescente:");
			for (int i = 10; i >= 1; i--) {
			System.out.println("A contagem está em: " + i); }
		}
	}

